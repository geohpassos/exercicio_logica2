var semana = parseInt(prompt("Digite número de 1-7: "))
if (semana == "1"){
    alert(semana+"-Domingo")
}
else if (semana == "2"){
    alert(semana+"-Segunda")
}
else if (semana == "3"){
    alert(semana+"-Terça-feira")
}
else if (semana == "4"){
    alert(semana+"-QUarta-feira")
}
else if (semana == "5"){
    alert(semana+"-Quinta-feira")
}
else if (semana == "6"){
    alert(semana+"-Sexta-feira")
}
else if (semana == "7"){
    alert(semana+"-Sábado")
}
else{
    alert("Valor inválido")
}